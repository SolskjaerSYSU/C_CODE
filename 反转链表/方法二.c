#define  _CRT_SECURE_NO_WARNINGS 1
#include <string.h>
//思路二：创建三个指针，完成原链表的反转（三指针法）
struct ListNode {
    int val;
    struct ListNode* next;

};
typedef struct ListNode ListNode;

struct ListNode* reverseList(ListNode* head)
{
	// 1. 初始化 n1 (最初没有“前一个”节点，所以是 NULL)
	ListNode* n1 = NULL;
	// 2. 初始化 n2 (从原链表的头节点开始打工)
	ListNode* n2 = head;
	// n3 先不用管，进循环再赋值
	ListNode* n3 = NULL;

	// 只要 n2 还没走到尽头，就继续翻转
	while (n2 != NULL)
	{
		// 步骤一 (防失联)：赶紧派 n3 记住下一个节点！
		n3 = n2->next;  //(我强烈建议把n3的初始化放在while里面，这样可以避免空链表的极端情况)
		// 步骤二 (翻转箭头)：让 n2 指向它的前一个节点 n1
		n2->next = n1;

		// 步骤三 (整体平移)：准备处理下一个人
		// n1 和 n2 就像毛毛虫一样，齐刷刷往后挪一步
		n1 = n2;// n1 来到 n2 的位置(切记不可用n1->next来取代n2，因为它俩压根就不是顺序关系！而且语句顺序也很重要，要让n1先到n2的位置才能移动n2)
		n2 = n3;// n2 来到 n3 (也就是下一个节点) 的位置（一定要让n2后移动）
	}

	// 循环结束时，n2 走到了 NULL，此时 n1 刚好停在原链表的最后一个节点上。
	// 原来的尾巴，现在变成了反转后新链表的头！
	return n1;
}

