// 5.为什么最后不用把这些指针置空（置为 NULL）？这不是好习惯吗？
//原因 1：我们没有释放任何内存
//在这个函数里，我们从头到尾都没有调用过 free()。所有的节点都还在内存里好好的，我们只是改变了它们之间“手牵手”的顺序。既然没有节点被炸毁，自然也就不存在指向废墟的“野指针”问题。
//
//原因 2：局部变量的生命周期
//n1、n2、n3 这三个指针变量，是你在 reverseList 函数内部定义的局部变量。
//在 C 语言中，局部变量是存放在“栈区”的。当这个函数执行完毕（走到 return 的那一刻），系统会自动、瞬间、彻底地将这三个变量销毁得一干二净。
//既然它们自己马上就要灰飞烟灭了，你再去给它们赋值为 NULL，就相当于在给一件马上要被扔进焚化炉的衣服洗去污渍，完全是多此一举。
//
//例外情况：什么时候必须置空？
//只有当你操作的是全局变量，或者是通过二级指针去修改函数外部依然存活的变量（比如之前尾删时的 * pphead = NULL）时，才必须手动置空。
