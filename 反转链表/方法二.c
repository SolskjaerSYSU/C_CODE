//1.为什么要设置三个节点，两个不可以吗？
//想象你现在站在链表的当前节点（我们叫它 n2）。你要做的事情是：把原本指向前面那个人的手，转过来指向后面那个人。
//
//为什么需要 n1（前一个节点）？
//如果只有 n2，你虽然想往回指，但你根本不知道排在你后面的人是谁！因为单链表只能往前看。所以，你必须安排一个指针 n1，永远站在 n2 的后方，充当“回头时的目标”。
//
//为什么需要 n3（后一个节点）？
//好，现在你有 n1 和 n2 了。你执行了 n2->next = n1; （把箭头反转了）。但是！反转完之后，n2 和原来前面的队伍断开连接了！你下一步该怎么往前走？你找不到下一个节点了！
//因此，在反转箭头之前，你必须提前派一个探路兵 n3，站在下一个节点的位置上守着。这样 n2 转身之后，还能顺着 n3 的位置继续往前走。
//
//总结三剑客的职责：
//
//n1(过去)：给 n2 提供反转后的指向目标。
//
//n2(现在)：负责执行反转动作的“打工人”。
//
//n3(未来)：负责在连接断开时，记住下一步往哪走的“探路兵”。
